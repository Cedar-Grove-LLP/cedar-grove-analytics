#!/usr/bin/env node
// Generate a SYNTHETIC, self-consistent stand-in for src/utils/invoicesRealData.mjs
// so the committed repo + CI never contain real attorney-compensation figures.
//
// Every monetary value is scaled by a constant factor. Because every relationship
// the workbook (and the test suite) relies on is LINEAR — column sums, the 10% CGF
// rate, waterfall differences, SUMIFS, P&L totals — a constant scale preserves all
// of them exactly, so the structural test suite still passes against the synthetic
// data. The rate ladder is replaced with a clean synthetic ladder that keeps the
// tests' invariants (ascending client rate, +12.5 attorney-rate chain).
//
// Reads the REAL data from a local (gitignored) source and writes the synthetic
// module. Never run in CI — this is a one-off local tool.
//
// Usage: node scripts/make-synthetic-invoices-data.mjs [sourceFile] [outFile]

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const SRC = process.argv[2] || join(ROOT, 'src/utils/invoicesRealData.local.mjs');
const OUT = process.argv[3] || join(ROOT, 'src/utils/invoicesRealData.mjs');

const K = 0.6137; // scale factor (arbitrary; obscures the real magnitudes)
// Numeric fields that are NOT money and must stay intact (counts, years, terms,
// row indices) so structural checks and date/year filters keep working.
const EXCLUDE_KEYS = new Set(['year', 'row', 'totalRows', 'paymentTerms']);

const { REAL_WORKBOOK } = await import(SRC);

const scale = (node, key) => {
  if (Array.isArray(node)) return node.map((v) => scale(v, key));
  if (node && typeof node === 'object') {
    const out = {};
    for (const [k, v] of Object.entries(node)) {
      if (k === 'rateSheet') { out[k] = v; continue; } // replaced wholesale below
      out[k] = scale(v, k);
    }
    return out;
  }
  if (typeof node === 'number' && !EXCLUDE_KEYS.has(key)) {
    // Round to 6 decimals — matches the extractor's num() so the assemble
    // round-trip test stays exact, while 6-decimal drift on column sums stays
    // far under the suite's 0.02 tolerance. (Rounding to CENTS would be too
    // coarse and break the sum-vs-stored-total checks.)
    return Math.round(node * K * 1e6) / 1e6;
  }
  return node;
};

const synthetic = scale(REAL_WORKBOOK, 'root');

// Clean synthetic rate ladder — 20 rows A1..P2 × A/B, ascending client rate,
// attorney rate +12.5 every row (satisfies the rate-sheet test's chain check).
const LEVELS = ['A1', 'A1', 'A2', 'A2', 'C1', 'C1', 'C2', 'C2', 'C3', 'C3', 'C4', 'C4', 'C5', 'C5', 'C6', 'C6', 'P1', 'P1', 'P2', 'P2'];
synthetic.rateSheet = LEVELS.map((level, i) => ({
  level,
  tier: i % 2 === 0 ? 'A' : 'B',
  clientRate: 300 + i * 20,
  attorneyRate: 150 + i * 12.5,
  colinRate: i >= 13 ? 200 + i * 8 : '',
  salary: level.startsWith('P') ? 'Variable' : 180000 + i * 10000,
  cravath: i % 2 === 0 && !level.startsWith('P') ? 220000 + i * 12000 : null,
}));

synthetic.source = 'SYNTHETIC fixture — scaled placeholder figures, not real data';
synthetic.extractedOn = REAL_WORKBOOK.extractedOn;

const header =
  '// SYNTHETIC stand-in for the Invoices (2026) workbook snapshot.\n' +
  '// Generated by scripts/make-synthetic-invoices-data.mjs — every monetary value\n' +
  '// is a constant-factor scaling of the real figures (relationships preserved so\n' +
  '// the test suite passes), so NO real attorney-compensation data lives in git.\n' +
  '// The real snapshot stays local-only (invoicesRealData.local.mjs, gitignored);\n' +
  '// the live "Real (live sheet)" mode reads the actual workbook at runtime.\n';
writeFileSync(OUT, `${header}export const REAL_WORKBOOK = ${JSON.stringify(synthetic, null, 1)};\n`);
console.log(`Wrote synthetic ${OUT} (${(readFileSync(OUT, 'utf8').length / 1024).toFixed(0)}KB)`);
